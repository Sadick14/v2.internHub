const SidebarMenuButton = React.forwardRef<
  HTMLButtonElement,
  React.ComponentProps<"button"> & {
    asChild?: boolean
    isActive?: boolean
    tooltip?: string | React.ComponentProps<typeof TooltipContent>
  }
>(
  (
    {
      asChild = false,
      isActive = false,
      tooltip,
      className,
      children,
      ...props
    },
    ref
  ) => {
    const Comp = asChild ? Slot : "button"
    const { state } = useSidebar();

    const button = (
      <Comp
        ref={ref}
        data-sidebar="menu-button"
        data-active={isActive}
        className={cn(
            "flex items-center p-3 text-primary-foreground/80 rounded-lg hover:bg-white/10 hover:text-primary-foreground w-full text-left",
            isActive && "bg-white/20 text-white font-semibold",
            state === 'collapsed' && "justify-center",
            className
        )}
        {...props}
      >
        {React.Children.map(children, (child) => {
             if (React.isValidElement(child) && child.type !== 'span') {
                return React.cloneElement(child as React.ReactElement<any>, {
                    className: cn(child.props.className, "text-lg")
                })
             }
             if (React.isValidElement(child) && child.type === 'span') {
                 return React.cloneElement(child as React.ReactElement<any>, {
                    className: cn(child.props.className, 'ml-3', state === 'collapsed' && 'hidden')
                 })
             }
             return child;
        })}
      </Comp>
    )

    if (!tooltip || state === 'expanded') {
      return button
    }
    
    if (typeof tooltip === "string") {
      tooltip = {
        children: tooltip,
      }
    }

    return (
      <Tooltip>
        <TooltipTrigger asChild>{button}</TooltipTrigger>
        <TooltipContent
          side="right"
          align="center"
          {...tooltip}
        />
      </Tooltip>
    )
  }
)
SidebarMenuButton.displayName = "SidebarMenuButton"

